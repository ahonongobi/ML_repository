import numpy as np

# Define the matrices
matrix1 = np.array([
    [-1, 3, -1],
    [-1, 3, -1],
    [-1, 3, -1]
])

matrix2 = np.array([
    [5, 3, 1],
    [5, 3, 1],
    [5, 3, 1]
])

# Perform matrix multiplication
result = np.dot(matrix1, matrix2)

# Print the result
print("Result of matrix multiplication:")
print(result)

